package com.subhechhu.daggertest.school;

public class Student {
    private String name;
    private String grades;
    private boolean isClassMonitor;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGrades() {
        return grades;
    }

    public void setGrades(String grades) {
        this.grades = grades;
    }

    public boolean getIsClassMonitor() {
        return isClassMonitor;
    }

    public void setIsClassMonitor(boolean isClassMonitor) {
        this.isClassMonitor = isClassMonitor;
    }
}
